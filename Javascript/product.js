function btn_ThemSP(){
    const main_product_card = document.querySelectorAll('.main-product-card');
    const add_to_cart = document.querySelectorAll('.add-to-cart');
    const icon_cart = document.querySelector('.btn-show-cart i');
    
    for(let i = 0; i < main_product_card.length; i++){
       
        main_product_card[i].addEventListener('mouseover', function(){

            add_to_cart[i].style = `visibility: visible; animation: animation-move-bottom-main-add-to-cart ease 0.3s forwards;`;

        });

        main_product_card[i].addEventListener('mouseleave', function(){

            add_to_cart[i].style = `visibility: hidden;`;            

        });

        add_to_cart[i].addEventListener('mouseup', function(){
            icon_cart.style = `animation: animation-rotate-cart-icon ease 0.5s`;
            
            setTimeout(function(){
                icon_cart.style = ``;
            }, 500);
        });
    }
}


